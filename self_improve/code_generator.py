    VALIDATOR_TEMPLATE = '''    "{key_type}": {{"min_len": {min_len}, "max_len": {max_len}, "prefix": "{prefix}"}},'''

    def __init__(self, notifier: Optional[DiscordNotifier] = None):
        self.notifier = notifier or DiscordNotifier()
        self.classifier = KeyClassifier()

    def generate_feature(self, description: str) -> Tuple[str, str]:
        """
        Generate Python code based on a natural language description.

        Args:
            description: Natural language description of the feature.

        Returns:
            Tuple of (generated_code, filename).
        """
        desc_lower = description.lower()

        # Determine what type of feature to generate
        if "scanner" in desc_lower or "scan" in desc_lower:
            return self._generate_scanner(description)
        elif "pattern" in desc_lower or "detect" in desc_lower or "classif" in desc_lower:
            return self._generate_pattern(description)
        elif "validator" in desc_lower or "valid" in desc_lower:
            return self._generate_validator(description)
        elif "notifier" in desc_lower or "report" in desc_lower:
            return self._generate_notifier(description)
        else:
            return self._generate_generic(description)

    def _generate_scanner(self, description: str) -> Tuple[str, str]:
        """Generate a new scanner module."""
        # Extract scanner name from description
        words = description.lower().split()
        scanner_name = "custom"
        for word in words:
            if word in ("for", "scanner", "scan", "new"):
                continue
            scanner_name = word
            break

        class_name = scanner_name.title().replace("_", "") + "Scanner"
        filename = f"{scanner_name}_scanner.py"

        code = self.SCANNER_TEMPLATE.format(
            scanner_name=scanner_name,
            class_name=class_name,
        )

        return code, filename

    def _generate_pattern(self, description: str) -> Tuple[str, str]:
        """Generate a new key classification pattern."""
        # Try to extract key type and pattern from description
        key_type = "unknown"
        pattern = "xox[baprs]-[a-zA-Z0-9-]{10,}"
        description_text = "Auto-generated pattern"
        prefix = "[redacted]"

        # Look for key type in description
        type_keywords = {
            "slack": ("slack", "xox[baprs]-[a-zA-Z0-9-]{10,}", "Slack token", "xox"),
            "aws": ("aws", "AKIA[0-9A-Z]{16}", "AWS Access Key ID", "AKIA"),
            "google": ("google", "AIza[0-9A-Za-z_-]{35}", "Google API key", "AIza"),
            "openai": ("openai", "sk-[a-zA-Z0-9]{20,}", "OpenAI API key", "sk-"),
            "claude": ("claude", "sk-ant-[a-zA-Z0-9_-]{20,}", "Anthropic/Claude API key", "sk-ant-"),
            "stripe": ("stripe", "sk_live_[a-zA-Z0-9]{24,}", "Stripe secret key", "sk_live_"),
            "discord": ("discord", r"[\w-]{24}\.[\w-]{6}\.[\w-]{27}", "Discord bot token", "[redacted]"),
        }

        desc_lower = description.lower()
        for keyword, (kt, pat, desc, pre) in type_keywords.items():
            if keyword in desc_lower:
                key_type = kt
                pattern = pat
                description_text = desc
                prefix = pre
                break

        # Generate pattern code
        code = self.PATTERN_TEMPLATE.format(
            key_type=key_type,
            pattern=pattern,
            description=description_text,
            prefix=prefix,
        )

        return code, f"{key_type}_pattern.py"

    def _generate_validator(self, description: str) -> Tuple[str, str]:
        """Generate a new validation rule."""
        key_type = "unknown"
        min_len = 20
        max_len = 200
        prefix = "sk-"

        desc_lower = description.lower()
        if "slack" in desc_lower:
            key_type, min_len, max_len, prefix = "slack", 10, 200, "xox"
        elif "aws" in desc_lower:
            key_type, min_len, max_len, prefix = "aws", 20, 20, "AKIA"
        elif "discord" in desc_lower:
            key_type, min_len, max_len, prefix = "discord", 59, 80, "None"

        code = self.VALIDATOR_TEMPLATE.format(
            key_type=key_type,
            min_len=min_len,
            max_len=max_len,
            prefix=prefix,
        )

        return code, f"{key_type}_validator.py"

